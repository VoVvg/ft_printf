#include "libft.h"

int			ft_word_count(char const *s, char c)
{
	int		count;

	count = 0;
	while (*s != '\0')
	{
		while (*s == c)
			s++;
		if (*s != c && *s != '\0')
		{
			while (*s != c && *s != '\0')
				s++;
			count++;
		}
	}
	return (count);
}
