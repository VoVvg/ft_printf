#include "libft.h"

void		*ft_memchr(const void *s, int c, size_t n)
{
	while (n > 0 && *(unsigned char *)s != (unsigned char)c)
	{
		n--;
		s++;
	}
	if (n == 0)
		return (NULL);
	return ((char *)s);
}
